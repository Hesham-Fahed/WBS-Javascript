COMPANY!
========

Struktur
========
Mitarbeiterdatenbank:
- Mitarbeiter (employees) haben einen Namen und ein Alter.
- Mitarbeiter arbeiten in einer Abteilung (department, dept).
- Abteilungen befinden sich an einem Standort (location)

- Mitarbeiter müssen immer einer Abteilung zugeordnet sein
- Abteilungen müssen immer einem Standort zugeordnet sein

Funktionalität
==============
login & logout (kann das Framework von Haus aus)

add location
delete location
show location

add department
delete department
show departments and their locations, ordered by dept name

add employee
delete employee
move Employee from one department to another
show all employees with departments and locations
- ordered by employee name
- ordered by department
- ordered by location
